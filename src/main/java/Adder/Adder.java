package Adder;

/**
 * The Adder.Adder class provides adding methods
 */
public class Adder {
    public Adder() {}

    /**
     * Adds x and y
     *
     * @param x value
     * @param y value
     * @return the sum
     */
    public int Add(int x, int y) {
        return x + y;
    }

    /**
     * Concatenates x and y
     *
     * @param x value
     * @param y value
     * @return the sum
     */
    public String Add(String x, String y) {
        return x + y;
    }
}


/**
 * The Adder class provides adding methods
 */
public class Adder {
    public Adder() {}

    /**
     * Adds x and y
     */
    public int Add(int x, int y) {
        return x + y;
    }

    /**
     * Concatenates x and y
     */
    public String Add(String x, String y) {
        return x + y;
    }
}

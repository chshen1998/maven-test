package divider;

/**
 * the divider
 */
public class Divider {
    public Divider() {}

    /**
     * Divs x and y
     *
     * @param x value
     * @param y value
     * @return the div
     */
    public int divide(int x, int y) {
        return x/y;
    }

    /**
     * Divs x and y
     *
     * @param x value
     * @param y value
     * @return the dic
     */
    public String divide(String x, String y) {
        return x + y;
    }
}

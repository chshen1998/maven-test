package Divider;

/**
 * Divider class provide dividing methods
 */
public class Divider {
    public Divider() {}

    /**
     * Divides x with y
     *
     * @param x value
     * @param y value
     * @return the div
     */
    public int divide(int x, int y) {
        return x/y;
    }

    /**
     * Divides x with y
     *
     * @param x value
     * @param y value
     * @return the div
     */
    public String divide(String x, String y) {
        return x + y;
    }
}
